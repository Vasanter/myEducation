# ГОРЯЧИЕ КЛАВИШИ:

# Ctrl + Alt + L — форматирование кода по стандарту (PEP 8 для Python).
# Ctrl + / — закомментировать или раскомментировать текущую строку.
# Ctrl + Shift + / — закомментировать или раскомментировать выделенный блок кода.
# Ctrl + D — дублировать строку или выделенный блок.
# Ctrl + Y — удалить строку.
# Ctrl + Shift + U — изменить регистр выделенного текста (верхний/нижний).
# Ctrl + Alt + Стрелка вверх / вниз — переместить текущую строку или выделенный блок вверх или вниз.
# Ctrl + W — выделение текущего блока кода (нажимать повторно для расширения выделения).
# Ctrl + Shift + W — уменьшить выделение.
# Ctrl + Space — автодополнение кода.
# Ctrl + B / Ctrl + клик по элементу — перейти к определению переменной, метода, класса.
# Ctrl + G — перейти к строке по номеру.
# Ctrl + Shift + N — поиск файла по имени.
# Double Shift (двойное нажатие Shift) — поиск по всему проекту (классы, файлы, действия).
# Alt + Enter — быстрые исправления и предложения по коду (например, импорт или улучшения).
# Ctrl + R — поиск и замена в текущем файле.
# Ctrl + Shift + F — поиск по всему проекту.
# Ctrl + E — просмотр последних открытых файлов.
# Shift + F6 — переименование переменной, функции, файла и т.д.
