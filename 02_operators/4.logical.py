"""ЛОГИЧЕСКИЕ ОПЕРАТОРЫ"""
# Используются для выполнения логических операций (часто вместе с операторами сравнения), возвращающих True или False.
# Приоритет логических операторов от высшего к низшему:
# not x - логическое НЕ (наивысший приоритет)
# and - логическое И
# or - логическое ИЛИ (низший приоритет)


# and — логическое И
# Если оба условия истинны, то оператор возвращает True, в противном случае — False:
# x = 5
# y = 8
# print(x > 1 and y < 10)  # True, потому что 5 больше 1 и 8 меньше 10.


# or — логическое ИЛИ
# Если хотя бы одно из условий истинно, оператор возвращает True. Если оба условия ложные — False:
# x = 5
# print(x < 1 or x > 10)  # False, потому что 5 не меньше 1 и не больше 10


# not — логическое НЕ
# not меняет логическое значение условия, перед которым он стоит, на противоположное (инвертирует).
# Другими словами, если значение выражения равно True, то оператор вернёт False и наоборот.
# x = 5
# y = 8
# print(not (x > 1 and y < 10)) # False, т.к. 5 больше 1 и y меньше 10, но оператор not инвертирует значение.

# Эти операторы часто используются в условиях и циклах, которые выполняют код при выполнении определённого условия.


# Полная таблица приоритета (для логических и сравнений):
# 1. ( )              - скобки
# 2. **               - возведение в степень
# 3. +x, -x, ~x       - унарные плюс, минус, битовое НЕ
# 4. *, /, //, %      - умножение, деление
# 5. +, -             - сложение, вычитание
# 6. <<, >>           - битовые сдвиги
# 7. &                - битовое И
# 8. ^                - битовое исключающее ИЛИ
# 9. |                - битовое ИЛИ
# 10. <, <=, >, >=, ==, !=, is, is not, in, not in  - сравнения
# 11. not             - логическое НЕ
# 12. and             - логическое И
# 13. or              - логическое ИЛИ
